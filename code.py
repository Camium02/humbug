# Calculate window size based on maze dimensions
WIDTH = len(MAZE[0]) * CELL_SIZE
HEIGHT = len(MAZE) * CELL_SIZE

# Create the game window
screen = pygame.display.set_mode((WIDTH, HEIGHT))
pygame.display.set_caption("Pac-Man Maze")

def draw_maze():
    screen.fill(BACKGROUND_COLOR)
    
    for y, row in enumerate(MAZE):
        for x, cell in enumerate(row):
            # Calculate the position for drawing
            rect_x = x * CELL_SIZE
            rect_y = y * CELL_SIZE
            
            if cell == 1:  # Wall
                pygame.draw.rect(screen, WALL_COLOR, (rect_x, rect_y, CELL_SIZE, CELL_SIZE))
            elif cell == 2:  # Pellet
                pygame.draw.circle(screen, PELLET_COLOR, 
                                  (rect_x + CELL_SIZE // 2, rect_y + CELL_SIZE // 2), 
                                  CELL_SIZE // 10)
            elif cell == 3:  # Power Pellet
                pygame.draw.circle(screen, POWER_PELLET_COLOR, 
                                  (rect_x + CELL_SIZE // 2, rect_y + CELL_SIZE // 2), 
                                  CELL_SIZE // 3)

# Main game loop
def main():
    running = True
    
    while running:
        for event in pygame.event.get():
            if event.type == pygame.QUIT:
                running = False
        
        draw_maze()
        pygame.display.flip()
    
    pygame.quit()
    sys.exit()

if __name__ == "__main__":
    main()