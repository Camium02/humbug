    def get_tile(self, x, y):
        """Get the type of tile at position (x, y)."""
        if 0 <= x < self.width and 0 <= y < self.height:
            return self.maze[y][x]
        return None
    
    def set_tile(self, x, y, tile_type):
        """Set the type of tile at position (x, y)."""
        if 0 <= x < self.width and 0 <= y < self.height:
            self.maze[y][x] = tile_type
            return True
        return False
    
    def is_wall(self, x, y):
        """Check if the tile at (x, y) is a wall."""
        return self.get_tile(x, y) == self.WALL
    
    def is_path(self, x, y):
        """Check if the tile at (x, y) is a path (empty or has dot/power pellet)."""
        tile = self.get_tile(x, y)
        return tile == self.EMPTY or tile == self.DOT or tile == self.POWER_PELLET
    
    def count_dots(self):
        """Count how many dots and power pellets are left in the maze."""
        dot_count = 0
        for y in range(self.height):
            for x in range(self.width):
                tile = self.get_tile(x, y)
                if tile == self.DOT or tile == self.POWER_PELLET:
                    dot_count += 1
        return dot_count
    
    def print_maze(self):
        """Print the maze in a readable format."""
        symbol_map = {
            self.EMPTY: ' ',      # Empty path
            self.WALL: '#',       # Wall
            self.DOT: '.',        # Dot
            self.POWER_PELLET: 'O', # Power Pellet
            self.GHOST_HOUSE: 'G'   # Ghost house
        }
        
        for row in self.maze:
            print(''.join(symbol_map[cell] for cell in row))

